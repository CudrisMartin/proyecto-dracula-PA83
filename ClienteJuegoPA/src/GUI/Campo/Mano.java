    /* AÃ±ade un nuevo sprite de carta a la Mano a partir del Id de la carta*/
    public void anadirTarjeta(int i){
        Casilla c = new Casilla(i);
        c.setVisible(true);
        c.addMouseListener(this.getMouseListeners()[0]);
        this.add(c);
        this.updateUI();
    } 
    
    /* Elimina el sprite de una carta que sale de la Mano*/
    public void eliminarCarta(int i) {
        this.remove(i);
        this.updateUI();
    }
    
