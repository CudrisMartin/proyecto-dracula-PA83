    @Override
    public void mouseClicked(MouseEvent e) {
        if (cartaSeleccionada == 0){
            if (e.getSource().getClass() == Casilla.class) {
                if (((Casilla) e.getSource()).getTipoCasilla() == 1){
                    this.cartaSeleccionada = ((Casilla) e.getSource()).getNumeroCarta();
                }
            }
        }
    }

