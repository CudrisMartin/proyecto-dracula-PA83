    public void anadirTarjeta(int i){
        Casilla c = new Casilla(i);
        c.setNumeroCarta(tamanoMano +1);
        this.tamanoMano += 1;
        c.setVisible(true);
        c.addMouseListener(this);
        this.add(c);
        this.updateUI();
    }
    
    

